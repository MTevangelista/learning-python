# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.
salarioPorHora = float(input('Quando você ganha por hora? '))
horasTrabalhadasNoMes = float(input('Informe as horas trabalhadas no mês: '))

result = salarioPorHora * horasTrabalhadasNoMes

print('O total do sálario é:', result)